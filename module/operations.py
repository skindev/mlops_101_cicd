def add(first: float, second: float):
    """
    :param first: first number to be summed
    :param second: second number to be summed
    :return: sum
    """
    return first + second

def subtract(first: float, second: float):
    """
    :param first: first number
    :param second: second number
    :return: difference (first - second)
    """
    return first - second

def multiply(first: float, second: float):
    """
    :param first: first number
    :param second: second number
    :return: product (first * second)
    """
    return first * second

def divide(first: float, second: float):
    """
    :param first: first number
    :param second: second number
    :return: division (first / second)
    """
    return first / second